    private void btnBuscarCodigoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarCodigoActionPerformed
        try {
            int codigo =Integer.parseInt(this.txCodigoEditar.getText().trim());
            Pelicula pelicula= new RegistroVideoClub().buscarPorCodigo(codigo);
            if(pelicula==null){
                javax.swing.JOptionPane.showMessageDialog(this,"No hay registros para el codigo: "+codigo);
            }else{
            this.txNombreEditar.setText(pelicula.getNombre());
            this.txPrecioEditar.setText(String.valueOf(pelicula.getPrecio()));
            //this.cmbDescripcion1.setSelectedIndex(pelicula.getId_categoria());
            cargarCMBBuscar(pelicula,pelicula.getId_categoria());
            if (pelicula.getFormato().equals("S")){
                this.checkFormatoE.setSelected(true);
            }else{
                checkFormatoE.setSelected(false);
            }
            
            
            }
            } catch (Exception e) {
                    JOptionPane.showMessageDialog(this, "No Ingreso Ningun Código");
        
        }
            
    }//GEN-LAST:event_btnBuscarCodigoActionPerformed

