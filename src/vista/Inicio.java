    public ResultSet sql_query(String query) {
        Conexion  cc = new Conexion();
        Connection cn = cc.getConexion();
        if (cn == null) {
            JOptionPane.showMessageDialog(this, "No conectado :(");
        } else {
            //JOptionPane.showMessageDialog(this, "Conectado :)");
            try {
                Statement stm = cn.createStatement();
                ResultSet rs = stm.executeQuery(query);
                return rs;

            } catch (SQLException e) {
                JOptionPane.showMessageDialog(this, "Error SQL: " + e.getMessage());
            }
        }
        return null;
    }
    
    
    public void mostrarDatos() throws SQLException {
        DefaultTableModel modelo = new DefaultTableModel();
        modelo.addColumn("CODIGO");
        modelo.addColumn("NOMBRE");
        modelo.addColumn("PRECIO");
        modelo.addColumn("DESCRIPCION");
        modelo.addColumn("FORMATO");
               
        jtListar.setModel(modelo);
        String[] arreglo = new String[5];

        ResultSet rs = sql_query("SELECT PELICULA.CODIGO, PELICULA.NOMBRE, PELICULA.PRECIO, CATEGORIA.DESCRIPCION,PELICULA.FORMATO\n" +
"FROM PELICULA,CATEGORIA WHERE PELICULA.activo=1 AND PELICULA.ID_CATEGORIA=CATEGORIA.ID" );
        while (rs.next()) {
            arreglo[0] = rs.getString(1);
            arreglo[1] = rs.getString(2);
            arreglo[2] = rs.getString(3);
            arreglo[3] = rs.getString(4);
            if(rs.getString(5).equals("S")){
                arreglo[4] = "4K";
            } else {
                arreglo[4] = "Sin 4K";
            }
            modelo.addRow(arreglo);
        }
        jtListar.setModel(modelo);
    }
    
    
