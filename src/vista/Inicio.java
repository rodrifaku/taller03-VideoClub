    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
      if (validarDocumento())
        if (tfLleno(txCodigo.getText()))
        if (tfLleno(txNombre.getText()))
        if (tfLleno(txPrecio.getText())){
            Pelicula pelicula = new Pelicula();
            
            pelicula.setCodigo(Integer.parseInt(txCodigo.getText()));
            if(pelicula.getCodigo()>= 10000 && pelicula.getCodigo()<= 99999){
                pelicula.setNombre(this.txNombre.getText().trim());
                if(pelicula.getNombre().length()>3){
                    pelicula.setPrecio(Integer.parseInt(this.txPrecio.getText()));
                    if(pelicula.getPrecio()> 1000){
                        String aux="N";
                        if (this.checkFormato.isSelected())
                            aux="S";
                        pelicula.setFormato(aux);
                        pelicula.setId_categoria(this.cmbDescripcion.getSelectedIndex());
                        pelicula.setActivo(true);  
                    }else{
                        javax.swing.JOptionPane.showMessageDialog(this,"Valor debe ser mayor a 1000  ");
                        }
                 }else{
                        javax.swing.JOptionPane.showMessageDialog(this,"Nombre debe ser Mayor a 3 Caracteres");
                        }   
            }else{
                javax.swing.JOptionPane.showMessageDialog(this,"Rango de Codigo 10000-99999");
                }
            if(new RegistroVideoClub().agregarPelicula(pelicula)){
                javax.swing.JOptionPane.showMessageDialog(this,"Registro Grabado!!");
            }else{
                javax.swing.JOptionPane.showMessageDialog(this,"No se grabo!!");
            }
            this.limpiarPestañaUno();
        }  
    }//GEN-LAST:event_btnAgregarActionPerformed

